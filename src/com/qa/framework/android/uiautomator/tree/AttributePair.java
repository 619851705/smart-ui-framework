package com.qa.framework.android.uiautomator.tree;

/**
 * The type Attribute pair.
 */
public class AttributePair {
    /**
     * The Key.
     */
    public String key, /**
     * The Value.
     */
    value;

    /**
     * Instantiates a new Attribute pair.
     *
     * @param key   the key
     * @param value the value
     */
    public AttributePair(String key, String value) {
        this.key = key;
        this.value = value;
    }
}
